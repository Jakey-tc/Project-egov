package com.bjpowernode.egov.beans;

public class Enterprise {
	
	
	private String orgcode;
	private String foreignNumber;
	private String chName;
	private String EnName;
	private String contactName;
	private String contactPhone;
	private int capital;
	private int foreignCapital;
	private String currency;
	private String manager;
	private String regdate;
	
	
	public Enterprise() {
		
		
	}
	
	public String getOrgcode() {
		return orgcode;
	}
	public void setOrgcode(String orgcode) {
		this.orgcode = orgcode;
	}
	public String getForeignNumber() {
		return foreignNumber;
	}
	public void setForeignNumber(String foreignNumber) {
		this.foreignNumber = foreignNumber;
	}
	public String getChName() {
		return chName;
	}
	public void setChName(String chName) {
		this.chName = chName;
	}
	public String getEnName() {
		return EnName;
	}
	public void setEnName(String enName) {
		EnName = enName;
	}
	public String getContactName() {
		return contactName;
	}
	public void setContactName(String contactName) {
		this.contactName = contactName;
	}
	public String getContactPhone() {
		return contactPhone;
	}
	public void setContactPhone(String contactPhone) {
		this.contactPhone = contactPhone;
	}
	public int getCapital() {
		return capital;
	}
	public void setCapital(int capital) {
		this.capital = capital;
	}
	public int getForeignCapital() {
		return foreignCapital;
	}
	public void setForeignCapital(int foreignCapital) {
		this.foreignCapital = foreignCapital;
	}
	public String getCurrency() {
		return currency;
	}
	public void setCurrency(String currency) {
		this.currency = currency;
	}
	public String getManager() {
		return manager;
	}
	public void setManager(String manager) {
		this.manager = manager;
	}
	public String getRegdate() {
		return regdate;
	}
	public void setRegdate(String regdate) {
		this.regdate = regdate;
	}
	
	
}              
